    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
       String username = this.username.getText();
        String password = new String(this.password.getPassword());

        try {
            Connection conn = DriverManager.getConnection(url, user, passwordDB);
            String query = "SELECT * FROM users WHERE username = ? AND password = ?";
            PreparedStatement stmt = conn.prepareStatement(query);
            stmt.setString(1, username);
            stmt.setString(2, password);

            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "Login successful!");
                
                // If "Remember Me" is checked, save credentials
                if (rememberme.isSelected()) {
                    saveRememberedCredentials(username, password); // Hash the password in production
                } else {
                    clearRememberedCredentials();
                }

                mainmenu l = new mainmenu();
                l.setVisible(true); // Proceed to the next screen or application logic
            } else {
                JOptionPane.showMessageDialog(this, "Invalid username or password.");
            }

            rs.close();
            stmt.close();
            conn.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        }
    
    }//GEN-LAST:event_loginActionPerformed

