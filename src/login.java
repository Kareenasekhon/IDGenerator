     private void loadRememberedCredentials() {
        Properties prop = new Properties();
        File configFile = new File(configFilePath);
        if (configFile.exists()) {
            try (InputStream input = new FileInputStream(configFilePath)) {
                prop.load(input);

                String rememberedUsername = prop.getProperty("username");
                String rememberedPassword = prop.getProperty("password"); // Decrypt if necessary

                if (rememberedUsername != null && rememberedPassword != null) {
                    username.setText(rememberedUsername);
                    password.setText(rememberedPassword); // Decrypt if necessary
                    rememberme.setSelected(true);
                }
            } catch (IOException e) {
                e.printStackTrace();
            }
        }
    }

    // Save the credentials if "Remember Me" is checked
    private void saveRememberedCredentials(String username, String password) {
        Properties prop = new Properties();
        try (OutputStream output = new FileOutputStream(configFilePath)) {
            prop.setProperty("username", username);
            prop.setProperty("password", password); // In production, hash the password
            prop.store(output, null);
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    // Clear the saved credentials if "Remember Me" is unchecked
    private void clearRememberedCredentials() {
        File configFile = new File(configFilePath);
        if (configFile.exists()) {
            configFile.delete(); // Delete the file
        }
    }
